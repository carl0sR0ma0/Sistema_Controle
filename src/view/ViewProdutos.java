    private void salvarProduto(){
        //SALVA um produto no banco
        modelProdutos.setCodPro(Integer.parseInt(this.jtfCodigo.getText()));
        modelProdutos.setProNome(this.jtfNome.getText());
        modelProdutos.setProTipo(this.jcbTipo.getSelectedItem().toString());
        modelProdutos.setProEstoque(Integer.parseInt(this.jtfEstoque.getText()));
        if (controllerProduto.salvarProdutoController(modelProdutos) > 0) {
            JOptionPane.showMessageDialog(this, "Produto cadastrado com sucesso!", "Atenção", JOptionPane.WARNING_MESSAGE);
            this.carregarProdutos();
            this.limparCampos();
            this.habilitareDesabilitarCampos(false);
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao cadastrar produto!", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void alterarProduto() {
        // ALTERAR um produto no banco
        modelProdutos.setCodPro(Integer.parseInt(this.jtfCodigo.getText()));
        modelProdutos.setProNome(this.jtfNome.getText());
        modelProdutos.setProTipo(this.jcbTipo.getSelectedItem().toString());
        modelProdutos.setProEstoque(Integer.parseInt(this.jtfEstoque.getText()));
        if (controllerProduto.alterarProdutoController(modelProdutos)) {
            JOptionPane.showMessageDialog(this, "Produto alterado com sucesso!", "Atenção", JOptionPane.WARNING_MESSAGE);
            this.carregarProdutos();
            this.limparCampos();
            this.habilitareDesabilitarCampos(false);
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao alterar produto!", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

    /**
     * Habilitar e desabilitar os campos do formulário
     *
     * @param condicao
     */
    private void habilitareDesabilitarCampos(boolean condicao) {
        jtfCodigo.setEnabled(condicao);
        jtfNome.setEnabled(condicao);
        jcbTipo.setEditable(condicao);
        jtfEstoque.setEnabled(condicao);
    }

    /**
     * Limpar os campos do formulário
     */
    private void limparCampos() {
        jtfCodigo.setText("");
        jtfNome.setText("");
        jtfEstoque.setText("");
        jcbTipo.setSelectedIndex(0);
        //jlblImagaem.se
    }

    /**
     * Preencher a tabela de produtos com os produtos cadastrado no banco
     */
    private void carregarProdutos() {
        listaModelProduto = controllerProduto.retornarListaProdutoController();
        DefaultTableModel modelo = (DefaultTableModel) jtProdutos.getModel();
        modelo.setNumRows(0);
        //Inserir Produtos na Tabela
        int cont = listaModelProduto.size();
        for (int i = 0; i < cont; i++) {
            modelo.addRow(new Object[]{
                listaModelProduto.get(i).getIdProduto(),
                listaModelProduto.get(i).getCodPro(),
                listaModelProduto.get(i).getProTipo(),
                listaModelProduto.get(i).getProNome(),
                listaModelProduto.get(i).getProEstoque()
            });

        }
    }
    
