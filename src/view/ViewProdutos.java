    private void jbAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAlterarActionPerformed
        // TODO add your handling code here:
        salvarAlterar = "alterar";
        habilitareDesabilitarCampos(true);
        int linha = jtProdutos.getSelectedRow();
        try {
            int codigoProduto = (int) jtProdutos.getValueAt(linha, 0);
            // Recuperar dados do banco
            modelProdutos = controllerProduto.retornarProdutoController(codigoProduto);
            // Setar na interface
            this.jtfCodigo.setText(String.valueOf(modelProdutos.getCodPro()));
            this.jtfNome.setText(modelProdutos.getProNome());
            this.jtfEstoque.setText(String.valueOf(modelProdutos.getProEstoque()));
            this.jcbTipo.setSelectedItem(modelProdutos.getProTipo().toString());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Código inválido ou nenhum registro selecionado!", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jbAlterarActionPerformed

